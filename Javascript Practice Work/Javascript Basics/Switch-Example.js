//Switch case for string

var str='x';  
var result;  
switch(str)
{  
    case 'A':  
        result="A Character";  
    break;  
    case 'B':  
        result="B Character";  
    break;  
    case 'C':  
        result="C Character";  
    break;  
    default:  
        result="Not Found this Character";  
}  
console.log(result); 

//For Integer

var num=10;

var res;
switch(num)
{
    case 5:  
        res="Got number 5";  
    break;  
    case 8:  
        res="Got number 8";  
    break;  
    case 10:  
        res="Got number 10";  
    break;  
    default:  
        res="Not Found";
}
console.log(res);